    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Usuario usuario = new Usuario();
        Usuario buscado = new Usuario();
        UsuarioDAO udao = new UsuarioDAO();
        GeradorCodigos codigo = new GeradorCodigos();
        String cod;
        do {

            cod = codigo.gerarCodigo("U");
        } while (!udao.buscarUsuario(cod).isEmpty());

        if ((ctNome.getText().isEmpty()) || (ctLogin.getText().isEmpty()) || (cpSenha.getText().isEmpty()) || cpConfirmar.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, " Preencha todos os campos! ");
        } else if (validate(ctEmail.getText())) {
            if (cpSenha.getText().equals(cpConfirmar.getText())) {
                buscado = udao.verificarLogin(ctLogin.getText());
                if (buscado.getLogin().equals("")) {
                    usuario.setCod(cod);
                    usuario.setNome(ctNome.getText());
                    usuario.setEmail(ctEmail.getText());
                    usuario.setLogin(ctLogin.getText());
                    usuario.setSenha(cpSenha.getText());
                    usuario.setTipo(cbTipo.getSelectedItem().toString());
                    udao.inserirUsuario(usuario);
                    JOptionPane.showMessageDialog(null, "Usuário(a) cadastrado(a) com Sucesso!", "Confirmação de Cadastro", JOptionPane.INFORMATION_MESSAGE);
                    resetCampos();
                    atualizarTabela();
                } else {

                    JOptionPane.showMessageDialog(null, "Já existe um usuário cadastrado com o login informado!", "Erro", JOptionPane.ERROR_MESSAGE);
                    ctLogin.setText("");
                }
            } else {

                JOptionPane.showMessageDialog(null, "Os campos : Senha e Confirmar Senha  estão diferentes");

            }
        } else {
            JOptionPane.showMessageDialog(null, "Email informado é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_btSalvarActionPerformed

