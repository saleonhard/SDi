    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (jtUsuarios.getSelectedRow() != -1) {

            Usuario usuario = new Usuario();
            UsuarioDAO u = new UsuarioDAO();

            int index = jtUsuarios.getSelectedRow();

            usuario.setCod((String) jtUsuarios.getValueAt(index, 0));
            usuario.setNome((String) jtUsuarios.getValueAt(index, 1));
            usuario.setLogin((String) jtUsuarios.getValueAt(index, 3));
            //usuario.setSenha((String) jtUsuarios.getValueAt(index, 3));
            usuario.setTipo((String) jtUsuarios.getValueAt(index, 2));

            u.deletarUsuario(usuario);
            JOptionPane.showMessageDialog(null, "Usuário(a) excluído(a) com Sucesso!", "Confirmação de Exclusão", JOptionPane.INFORMATION_MESSAGE);
            resetCampos();
            btSalvar.setEnabled(true);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "   Selecione um Usuário(a)", "Erro", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed
        Usuario buscado = new Usuario();

        if ((ctNome.getText().isEmpty()) || ctEmail.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, " Preencha todos os campos! ");
        } else {

            Usuario usuario = new Usuario();
            UsuarioDAO u = new UsuarioDAO();

            if (validate(ctEmail.getText())) {

                buscado = u.buscarUsuario3(ctCod.getText());
                if ((buscado.getLogin().equals("")) || (!buscado.getLogin().equals("")) && log.equals(ctLogin.getText())) {
                    usuario.setCod(ctCod.getText());
                    usuario.setNome(ctNome.getText());
                    usuario.setEmail(ctEmail.getText());
                    usuario.setLogin(buscado.getLogin());
                    usuario.setTipo(cbTipo.getSelectedItem().toString());
                    usuario.setSenha(buscado.getSenha());
                    u.alterarUsuario(usuario);
                    JOptionPane.showMessageDialog(null, "Usário(a) atualizada(a) com Sucesso!", "Confirmação de Atualização", JOptionPane.INFORMATION_MESSAGE);
                    resetCampos();
                    btSalvar.setEnabled(true);
                    atualizarTabela();
                } else {

                    JOptionPane.showMessageDialog(null, " Atualização não realizada, Usuário não existe!");

                }

            } else {
                JOptionPane.showMessageDialog(null, "Email informado é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);

            }

        }
    }//GEN-LAST:event_btAtualizarActionPerformed

    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        if (ctBusca.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "     Campo de busca vazio! ");
        } else {

            DefaultTableModel modelo = (DefaultTableModel) jtUsuarios.getModel();
            modelo.setNumRows(0);
            UsuarioDAO udao = new UsuarioDAO();
            List<Usuario> buscado = new ArrayList();
            buscado = udao.buscarUsuario2(ctBusca.getText());

            if (buscado.isEmpty()) {
                JOptionPane.showMessageDialog(null, "   Usuário não existe!");
                atualizarTabela();
            } else {
                for (Usuario usuario : buscado) {
                    modelo.addRow(new Object[]{
                        usuario.getCod(), usuario.getNome(), usuario.getTipo(), usuario.getLogin()

                    });

                }
            }

        }
    }//GEN-LAST:event_btBuscarActionPerformed

