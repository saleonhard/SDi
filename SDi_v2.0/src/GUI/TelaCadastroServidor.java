    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (jtServidores.getSelectedRow() != -1) {

            Servidor servidor = new Servidor();
            ServidorDAO s = new ServidorDAO();

            int index = jtServidores.getSelectedRow();

            servidor.setCod((String) jtServidores.getValueAt(index, 0));
            servidor.setNome((String) jtServidores.getValueAt(index, 1));
            servidor.setEmail((String) jtServidores.getValueAt(index, 2));
            servidor.setEstado((String) jtServidores.getValueAt(index, 3));
            servidor.setCampus((String) jtServidores.getValueAt(index, 4));

            s.deletarServidor(servidor);
            JOptionPane.showMessageDialog(null, "Servidor(a) excluído(a) com Sucesso!", "Confirmação de Exclusão", JOptionPane.INFORMATION_MESSAGE);
            resetCampos();
            btSalvar.setEnabled(true);
            atualizarTabela(SQL_LISTAR_TODOS);
        } else {
            JOptionPane.showMessageDialog(null, "   Selecione um Servidor(a)", "Erro", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed

        if ((ctNome.getText().isEmpty()) || (ctEmail.getText().isEmpty())) {

            JOptionPane.showMessageDialog(null, " Preencha todos os campos! ");
        } else  if(validate(ctEmail.getText())) {

            Servidor servidor = new Servidor();
            ServidorDAO s = new ServidorDAO();

            servidor.setCod(ctCodigo.getText());;
            servidor.setNome(ctNome.getText());
            servidor.setEmail(ctEmail.getText());
            servidor.setEstado(cbTipo.getSelectedItem().toString());
            servidor.setCampus(cbCampus.getSelectedItem().toString());
            
            
            
            if (!s.buscarServidores(ctCodigo.getText()).isEmpty()) {
                s.atualizarServidor(servidor);
                JOptionPane.showMessageDialog(null, "Servidor(a) atualizada(a) com Sucesso!", "Confirmação de Atualização", JOptionPane.INFORMATION_MESSAGE);
                resetCampos();
                btSalvar.setEnabled(true);
                atualizarTabela(SQL_LISTAR_TODOS);
            } else {

                JOptionPane.showMessageDialog(null, " Atualização não realizada, Servidor não existe!");

            }

        }else{
          JOptionPane.showMessageDialog(null, "Formato do email informado é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
           
        }


    }//GEN-LAST:event_btAtualizarActionPerformed

