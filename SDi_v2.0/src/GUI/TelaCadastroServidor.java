    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Servidor servidor = new Servidor();
         Servidor buscado = new Servidor();
        ServidorDAO sdao = new ServidorDAO();
        GeradorCodigos codigo = new GeradorCodigos();
        String cod;
        do {

            cod = codigo.gerarCodigo("S");
        } while (!sdao.buscarServidores(cod).isEmpty());

        servidor.setCod(cod);
        servidor.setNome(ctNome.getText());
        servidor.setEmail(ctEmail.getText());
        servidor.setEstado(cbTipo.getSelectedItem().toString());
        servidor.setCampus(cbCampus.getSelectedItem().toString());

        if ((ctNome.getText().isEmpty()) || (ctEmail.getText().isEmpty())) {

            JOptionPane.showMessageDialog(null, " Preencha todos os campos! ");
        } else if(validate(ctEmail.getText())) {
            buscado = sdao.verificarEmail(ctEmail.getText());
            if(buscado.getEmail().equals("")){
            sdao.inserirServidor(servidor);
            JOptionPane.showMessageDialog(null, "Servidor(a) cadastrado(a) com Sucesso!", "Confirmação de Cadastro", JOptionPane.INFORMATION_MESSAGE);
            resetCampos();
            atualizarTabela(SQL_LISTAR_TODOS);
             }else{
          JOptionPane.showMessageDialog(null, "Email informado já está cadastrado!", "Erro", JOptionPane.ERROR_MESSAGE);
           
        }

        }else{
          JOptionPane.showMessageDialog(null, "Email informado é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
           
        }


    }//GEN-LAST:event_btSalvarActionPerformed

