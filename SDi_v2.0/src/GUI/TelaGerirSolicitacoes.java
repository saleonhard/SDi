    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        if (ctBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "     Campo de busca vazio! ");
        } else {

            DefaultTableModel modelo = (DefaultTableModel) jtSolicitacoes.getModel();
            modelo.setNumRows(0);
            SolicitacaoDAO sdao = new SolicitacaoDAO();
            List<Solicitacao> buscado = new ArrayList();
            buscado = sdao.buscarSolicitacao2(ctBuscar.getText());

            if (buscado.isEmpty()) {
                JOptionPane.showMessageDialog(null, "   Solicitação não existe!");
                atualizarTabela();
            } else {
                for (Solicitacao solicitacao : buscado) {
                    modelo.addRow(new Object[]{
                        solicitacao.getCod(), solicitacao.getAssunto(), solicitacao.getConteudo(),solicitacao.getStatus()

                    });

                }
            }

        }
    }//GEN-LAST:event_btBuscarActionPerformed

