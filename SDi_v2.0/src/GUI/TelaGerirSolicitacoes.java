    private void btEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEnviarActionPerformed
        Solicitacao solicitacao = new Solicitacao();
        SolicitacaoDAO sdao = new SolicitacaoDAO();
        GeradorCodigos codigo = new GeradorCodigos();
        String cod;
        do {

            cod = codigo.gerarCodigo("A");
        } while (!sdao.buscarSolicitacao(cod).isEmpty());

        solicitacao.setCod(cod);
        solicitacao.setAssunto(ctAssunto.getText());
        solicitacao.setConteudo(ctConteudo.getText());
        solicitacao.setStatus("Pendente");
        
       

        if ((ctAssunto.getText().isEmpty()) || (ctConteudo.getText().isEmpty())) {

            JOptionPane.showMessageDialog(null, " Preencha todos os campos! ");
        } else {

            sdao.inserirSolicitacao(solicitacao);
            JOptionPane.showMessageDialog(null, "Solicitação enviada com Sucessso!", "Confirmação de Envio", JOptionPane.INFORMATION_MESSAGE);
            atualizarTabela();
            resetCampos();
           
        }

    }//GEN-LAST:event_btEnviarActionPerformed

