    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
       if (jtCampus.getSelectedRow() != -1) {

            Campus campus = new Campus();
            CampusDAO c = new CampusDAO();

            int index = jtCampus.getSelectedRow();

            campus.setCod((String) jtCampus.getValueAt(index, 0));
            campus.setNome((String) jtCampus.getValueAt(index, 1));
            campus.setLocal((String) jtCampus.getValueAt(index, 2));
            

            c.deletarCampus(campus);
            JOptionPane.showMessageDialog(null, "Campus excluído com Sucesso!", "Confirmação de Exclusão", JOptionPane.INFORMATION_MESSAGE);
            resetCampos();
            btSalvar.setEnabled(true);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "   Selecione um Campus", "Erro", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed
        if ((ctNome.getText().isEmpty()) || (ctLocal.getText().isEmpty())) {

            JOptionPane.showMessageDialog(null, " Preencha todos os campos! ");
        } else {
            
            Campus campus = new Campus();
            CampusDAO c = new CampusDAO();
            

            campus.setCod(ctCodigo.getText());;
            campus.setNome(ctNome.getText());
            campus.setLocal(ctLocal.getText());
            
            if (!c.buscarCampus(ctCodigo.getText()).isEmpty()) {
                c.alterarCampus(campus);
                JOptionPane.showMessageDialog(null, "Campus atualizado com Sucesso!", "Confirmação de Atualização", JOptionPane.INFORMATION_MESSAGE);
                resetCampos();
                btSalvar.setEnabled(true);
                atualizarTabela();
            } else {

                JOptionPane.showMessageDialog(null, " Atualização não realizada, Campus não existe!");

            }

        }
    }//GEN-LAST:event_btAtualizarActionPerformed

