    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Campus campus = new Campus();
        CampusDAO cdao = new CampusDAO();
        GeradorCodigos codigo = new GeradorCodigos();
        String cod;
        do {

            cod = codigo.gerarCodigo("C");
        } while (!cdao.buscarCampus(cod).isEmpty());

        campus.setCod(cod);
        campus.setNome(ctNome.getText());
        campus.setLocal(ctLocal.getText());
        

        if ((ctNome.getText().isEmpty()) || (ctLocal.getText().isEmpty())) {

            JOptionPane.showMessageDialog(null, " Preencha todos os campos! ");
        } else {

            cdao.inserirCampus(campus);
            JOptionPane.showMessageDialog(null, "Campus cadastrado com Sucesso!", "Confirmação de Cadastro", JOptionPane.INFORMATION_MESSAGE);
            resetCampos();
            atualizarTabela();
            TelaCadastroServidor.setCBCampus();
        }

    }//GEN-LAST:event_btSalvarActionPerformed

